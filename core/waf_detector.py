"""
Blaze WAF Detector - Comprehensive Web Application Firewall detection.
Detects 20+ WAF/CDN providers through header analysis, cookie inspection,
body pattern matching, and behavioral analysis.
"""

import re
from dataclasses import dataclass, field
from typing import Dict, List, Optional


@dataclass
class WAFResult:
    detected: bool = False
    waf_names: List[str] = field(default_factory=list)
    confidence: Dict[str, float] = field(default_factory=dict)
    details: Dict[str, str] = field(default_factory=dict)

    def add(self, name: str, confidence: float, detail: str = ""):
        if name not in self.waf_names:
            self.waf_names.append(name)
        self.confidence[name] = max(self.confidence.get(name, 0), confidence)
        if detail:
            self.details[name] = detail
        self.detected = True

    def merge(self, other: "WAFResult"):
        for name in other.waf_names:
            self.add(
                name,
                other.confidence.get(name, 0.5),
                other.details.get(name, ""),
            )


