# Which WAFs should trigger an automatic stop recommendation
CRITICAL_WAFS = {
    "Cloudflare",
    "Akamai",
    "Imperva/Incapsula",
    "AWS WAF",
    "F5 BIG-IP ASM",
}


class WAFDetector:
    def __init__(self):
        self.signatures = WAF_SIGNATURES
        self.critical_wafs = CRITICAL_WAFS

    def detect(
        self, headers: Dict, body: str, cookies: Dict
    ) -> WAFResult:
        result = WAFResult()

        for waf_name, sigs in self.signatures.items():
            confidence = 0.0
            detail_parts = []

            # Check headers
            for header_name, pattern in sigs.get("headers", {}).items():
                for resp_header, resp_value in headers.items():
                    if resp_header.lower() == header_name.lower():
                        if re.search(pattern, str(resp_value)):
                            confidence += 0.4
                            detail_parts.append(
                                f"Header: {resp_header}={resp_value}"
                            )

            # Check cookies
            cookie_str = " ".join(
                f"{k}={v}" for k, v in cookies.items()
            )
            all_cookies = list(cookies.keys())
            for cookie_pattern in sigs.get("cookies", []):
                for cookie_name in all_cookies:
                    if re.search(cookie_pattern, cookie_name, re.IGNORECASE):
                        confidence += 0.3
                        detail_parts.append(f"Cookie: {cookie_name}")

            # Check body patterns
            for body_pattern in sigs.get("body", []):
                if re.search(body_pattern, body, re.IGNORECASE):
                    confidence += 0.5
                    detail_parts.append(f"Body pattern: {body_pattern[:50]}")

            if confidence >= 0.3:
                result.add(
                    waf_name,
                    min(confidence, 1.0),
                    "; ".join(detail_parts[:3]),
                )

        return result

    def is_waf_block(
        self, status_code: int, headers: Dict, body: str
    ) -> bool:
        """Quick check if a response looks like a WAF block (used during scanning)."""
        if status_code not in (403, 406, 429, 503, 1020):
            return False

        block_indicators = [
            r"Access Denied",
            r"Request blocked",
            r"Forbidden",
            r"blocked by",
            r"security policy",
            r"web application firewall",
            r"rate limit",
            r"too many requests",
            r"Your IP",
            r"has been blocked",
            r"DDoS protection",
            r"Checking your browser",
            r"challenge-platform",
        ]

        for pattern in block_indicators:
            if re.search(pattern, body, re.IGNORECASE):
                return True

        # Check specific WAF headers
        waf_headers = [
            "cf-ray",
            "x-sucuri-id",
            "x-akamai-transformed",
            "x-iinfo",
        ]
        for h in waf_headers:
            if h in {k.lower() for k in headers.keys()}:
                if status_code in (403, 503):
                    return True

        return False

    def is_critical(self, waf_result: WAFResult) -> bool:
        """Check if detected WAFs include critical ones that should stop scanning."""
        return any(
            name in self.critical_wafs for name in waf_result.waf_names
        )
